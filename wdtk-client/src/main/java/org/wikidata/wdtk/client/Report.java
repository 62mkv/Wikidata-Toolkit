import java.io.BufferedWriter;
import java.io.File;
import java.io.FileWriter;
import java.io.IOException;
import java.util.ArrayList;
import java.util.List;

/**
 * This class represent a report which contains messages which gives you
 * information which kind of outputs were generated by a
 * {@link DumpProcessingAction}. It gives you the possiblity to print these
 * messages into a file.
 * 
 * @author Michael GÃ¼nther
 * 
 */
public class Report {
	final List<String> entries = new ArrayList<String>();

	String reportFilename;

	boolean printReport = false;

	public void setReportFile(String reportFilename) {
		this.reportFilename = reportFilename;
		this.printReport = true;
	}

	public void addEntry(String entry) {
		entries.add(entry);
	}

	public String createReport() {
		StringBuilder builder = new StringBuilder();
		for (String entry : entries) {
			builder.append(entry);
			builder.append(System.getProperty("line.separator"));
		}

		return builder.toString();
	}

	public void printReport() throws IOException {
		if (printReport) {
			File reportFile = new File(this.reportFilename);
			BufferedWriter writer = new BufferedWriter(new FileWriter(
					reportFile));
			writer.write(this.createReport());
			writer.close();
		}
	}
}
